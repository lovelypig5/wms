<template>
<div class="panel panel-primary good-list">
    <div class="panel-heading">入库记录</div>
    <table class="table" v-if="!loading.fetch && inList.length > 0">
        <thead>
            <tr>
                <th>商品名称</th>
                <th>商品属性</th>
                <th>入库数量</th>
                <th>入库价格</th>
                <th>入库时间</th>
                <th>操作</th>
            </tr>
        </thead>
        <tbody>
            <tr class="item" v-for="record in inList">
                <td><a href="javascript:void(0)" v-link="{name: 'detail', params: {id: record.goods_id} }">{{record.good.name}}</a></td>
                <td>{{record.goods_attr | remove-whitespace}}</td>
                <td v-if="!record.edit">
                    {{record.amount}}
                </td>
                <td v-if="record.edit">
                    <input type="text" v-model="record.amount">
                </td>
                <td v-if="!record.edit">{{record.price}}
                </td>
                <td v-if="record.edit">
                    <input type="text" v-model="record.price">
                </td>
                <td>{{record.date}}</td>
                <td><i class="fa fa-pencil-square-o" aria-hidden="true" @click="edit(record)" v-if="!record.edit"></i>
                    <i class="fa fa-floppy-o" aria-hidden="true" v-if="record.edit" @click="save(record)"></i>
                </td>
            </tr>
        </tbody>
    </table>
    <pagination :pagination="params" :change="change"></pagination>
    <div class="empty" v-if="!loading.fetch && inList.length == 0">
        <div class="msg">没有入库记录</div>
    </div>
    <div v-if="loading.fetch">
        <div class="loading audio-wave"></div>
    </div>
</div>
</template>
<script>
import API from '../../config/api';
import actions from '../../vuex/actions';
import Pagination from '../../common/pagination.vue';

var InList = Vue.extend({
    name: 'inList',
    components: {
        pagination: Pagination
    },
    data() {
        return {
            inList: [],
            loading: {
                fetch: false
            },
            params: {
                page: 1,
                pageSize: 10,
                count: 0
            }
        }
    },
    ready() {
        this.fetch(true);
    },
    methods: {
        edit(record) {
            Vue.set(record, 'edit', !record.edit);
        },
        save(record) {
            var self = this;
            record.type = -1;
            $.ajax({
                url: API.modifyInOut,
                type: 'post',
                data: JSON.stringify(record),
                success(resp) {

                },
                error(resp) {
                    self.alert({
                        show: true,
                        msg: resp || '修改入库记录失败',
                        type: 'error'
                    })
                }
            }).always(() => {
                Vue.set(record, 'edit', !record.edit);
            })
        },
        change(page) {
            this.params.page = page;
            this.fetch();
        },
        fetch(reset) {
            var self = this;

            if (reset) {
                self.params = {
                    page: 1,
                    pageSize: 10,
                    count: 0
                }
            }

            if (self.loading.fetch) {
                return;
            }
            self.loading.fetch = !self.loading.fetch;

            $.ajax({
                url: API.inList,
                data: self.params,
                success(resp) {
                    self.inList = resp.content;
                    self.params.count = Math.ceil(resp.count / self.params.pageSize);
                },
                error(resp) {
                    self.alert({
                        show: true,
                        msg: '拉取入库记录失败',
                        type: 'error'
                    })
                }
            }).always(() => {
                self.loading.fetch = !self.loading.fetch;
            })
        }
    },
    vuex: {
        actions: {
            alert: actions.alert
        }
    }
})
export default InList;
</script>
